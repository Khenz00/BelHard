number_1= int(input('Введите первое число: '))
operation=input('Введите операцию')
number_2= int(input('Введите второе число: '))

if operation == '+':
 print(number_1+number_2)

elif operation == '-':
 print(number_1-number_2)

elif operation == '*':
 print(number_1*number_2)

elif operation == '/':
 print(number_1/number_2)



